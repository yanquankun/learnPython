# 面向对象编程（OOP）与面向过程编程的区别：
# • 面向对象比面向过程更适合复杂度高的编码需求
# • 面向对象适合有更严格的封装需求的程序
# • 面向对象可设计的编程模式更丰富
# • 面向对象涉及的技术概念更多
# 总结：面向对象编程（OOP）强调对象的封装、继承和多态，而面向过程编程则强调函数和过程的调用。

# 面向对象编程的核心概念包括类、对象、继承和多态。
# 面向过程编程则更关注函数的调用和数据的处理。

# 面向过程：
# ✦ 以过程为核心，强调解决问题的流程
# ✦ 先 xxx 再 xxx 然后 xxx
# ✦ 特点：符合时间顺序，易于理解程序逻辑

# 面向对象：
# • 和面向过程编程比起来，面向对象编程更关注一个对象的定义
# • 优点是更容易抽象和复用
