# 常见的数据类型

# 常量：None
# 逻辑值：True、False
# 空集：''、()、[]、{}、set()、range(0)
# 数字：int、float
# 序列：list、tuple、range
# 文本序列：str
# 集合：set
# 映射：dict

# 常见的数据类型的使用场景
# 不同类型的比较都可以使用 >、<、== ，但是比较的规则不同
# 1. 数字之间按照数值大小比较
# 2. 字符串之间按照首字母 ASCII 编码大小比较
# 3. 列表、元组从第一个元素开始比较

# 列表、元组、集合、字典可以和字符串、数字组成复杂的数据类型
# • 当你有多种组合方法时，要多考虑每种类型的内置方法
#
# 利用不同类型的优点，可以有效减少编程的难度，如：
# • 集合可以自动去重复，用于避免重复元素最便捷
# • 元组不可变，利用元组可以避免误修改
